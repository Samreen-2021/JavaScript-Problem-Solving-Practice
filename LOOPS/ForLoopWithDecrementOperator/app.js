// Create a for loop.

// Take the loop iterating variable as i. 
// Initialize it with 200
// Print numbers from 200 till 0.
// Use the decrement operator as -=3. 

for (let i=200; i>=0; i-=3){
    document.write(i +"<br>");
}

// it cant go to zero due to decrement of 3
for (let i=200; i>=0; i--){
    console.log(i);
}
